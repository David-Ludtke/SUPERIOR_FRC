(() => {
    const fullpath = document.currentScript.getAttribute('src');
    const path = fullpath.slice(0, fullpath.indexOf('j') - 1);
    const html = document.createElement('html');
    const head = document.createElement('head');
    const title = document.createElement('title');
    const favicon = document.createElement('link');
    const encoding = document.createElement('meta');
    const headcss = document.createElement('link');
    const bodycss = document.createElement('link');
    const buttoncss = document.createElement('link');
    const body = document.createElement('body');
    const home = document.createElement('button');
    const titlemessage = document.createElement('h1');
    html.lang = 'en';
    title.textContent = "Page Not Done";
    favicon.rel = 'icon';
    favicon.href = `${path}/img/SUPERIOR FRC.png`;
    favicon.type = 'image/png';
    encoding.charset = 'utf-8';
    headcss.rel = 'stylesheet';
    headcss.href = `${path}/css/header.css`;
    bodycss.rel = 'stylesheet';
    bodycss.href = `${path}/css/body.css`;
    buttoncss.rel = 'stylesheet';
    buttoncss.href = `${path}/css/button.css`;
    home.textContent = "Home";
    titlemessage.textContent = "Page Is Not Done Yet";
    home.addEventListener('click', function() {
        window.location.href = `${path}/html/home.html`;
    });
    home.style.marginLeft = '5px';
    head.appendChild(title);
    head.appendChild(favicon);
    head.appendChild(encoding);
    head.appendChild(headcss);
    head.appendChild(bodycss);
    head.appendChild(buttoncss);
    body.appendChild(home);
    body.appendChild(titlemessage);
    html.appendChild(head);
    html.appendChild(body);
    document.replaceChild(html, document.documentElement);
    body.classList.add('display');
})();